int	ft_sqrt(int nb)
{
	int	b;

	b = 1;
	if (nb > 0)
	{
		while (b * b <= nb)
		{
			if (b * b == nb)
			{
				return (b);
			}
			else if (b >= 46341)
			{
				return (0);
			}
			b++;
		}
	}
	return (0);
}
